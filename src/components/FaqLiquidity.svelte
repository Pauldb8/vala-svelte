<p>
  Liquidity are funds that are being used to pay out winning shares if no sufficient loosing shares exist. The more
  liquidity a market contains, the smaller the changes in price are when shares get bought or sold. This makes the
  market more attractive for investors who can bet with larger sums and lesser impact on their potential profits.
  <br />
  Liquidity can be added and removed anytime but will get used up depending on the share rates.
</p>

<h2>Why should I provide liquidity?</h2>

<p>
  If you are interested in a certain prediction, you can attract more investors to a market and increase its predictive
  ability and precision by providing liquidity.
  <br />
  If you are the market creator you can balance the fee you take for each trade with your invested liquidity, depending on
  how popular you expect your market to be. More liquidity might increase the trades, making you more profit.
</p>

