<p>
  Oracles are the arbiters or a market. They resolve the market by voting on one of the event outcomes. To make sure
  that you trust them you should check their voting history!
</p>

<h2>Proof of Burn</h2>

<p>
  To convince you of their trustworthiness, Oracles can burn Bitcoin and thereby signal their commitment to future
  revenue. If they cheat, they will have to start from 0 again. In the future this will be replaced with Proof of Work.
</p>

<h2>Market Commitment</h2>

<p>
  Before voting Oracles have to commit to resolving a market in the future. Thereby they accept the obligation to vote
  truthfully and timely on the market outcome.
</p>

